declare(strict_types=1);

namespace BaksDev\Wildberries\Products\Messenger\Cards\CardGroup;


use BaksDev\Wildberries\Products\Api\Cards\UpdateGroupWildberriesProductCardsRequest;
use Psr\Log\LoggerInterface;
use Symfony\Component\DependencyInjection\Attribute\Target;
use Symfony\Component\Messenger\Attribute\AsMessageHandler;

/** Метод обновляет группу карточки */
#[AsMessageHandler(priority: 0)]
final readonly class WildberriesCardGroupDispatcher
{
    public function __construct(
        #[Target('wildberriesProductsLogger')] private LoggerInterface $logger,
        private UpdateGroupWildberriesProductCardsRequest $UnionWildberriesProductCardsRequest,
    ) {}

    public function __invoke(WildberriesCardGroupMessage $message): void
    {
        $isUpdate = $this->UnionWildberriesProductCardsRequest
            ->forTokenIdentifier($message->getIdentifier())
            ->nomenclature($message->getNomenclature())
            ->group($message->getGroup())
            ->update();

        if($isUpdate)
        {
            $this->logger->info('Объединили карточку товара в группу');
            return;
        }

        $this->logger->critical(
            'wildberries-products: Ошибка при объединении карточку товара в группу',
            [self::class.':'.__LINE__, var_export($message, true)],
        );
    }
}
